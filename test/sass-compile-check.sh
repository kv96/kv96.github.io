#!/bin/bash

# ==============================================================================
# FUNCTIONS - START
# ==============================================================================

run_it()
{
	
	local _src="${1-scss/style.scss}"
	local _dest="${2-css-test/style.css}"
	local _options="${3---style=compressed}"

	# ----------------------------------------------------------------------------
	
	local _dest_dir="$(dirname "${_dest}")"
	
	if [ ! -d "${_dest_dir}" ]; then
	
		mkdir -p "${_dest_dir}"
	
	fi

	# ----------------------------------------------------------------------------
	
	sass "${_src}" "${_dest}" ${_options}
	
}
# run_it()

# ==============================================================================
# FUNCTIONS - END
# ==============================================================================


# ==============================================================================
# EXECUTION - START
# ==============================================================================

run_it $@

